const MATRIX_ITEMS = 420;

const buildSequence = () =>
  Array.from({ length: MATRIX_ITEMS }, (_, index) => SYMBOLS[index % SYMBOLS.length]);

export const MatrixPattern = memo(() => {
  const chars = useMemo(() => buildSequence(), []);

  return (
    <div className="matrix-pattern" aria-hidden>
      {chars.map((char, index) => (
        <span key={`${char}-${index}`}>{char}</span>
      ))}
    </div>
  );
});

MatrixPattern.displayName = 'MatrixPattern';
